# True values
print(bool(1))          # int
print(bool(3.5))        # float
print(bool("Python"))   # string
print(bool([1, 2]))     # list

# False values
print(bool(0))          # int
print(bool(0.0))        # float
print(bool(""))         # empty string
print(bool([]))         # empty list

